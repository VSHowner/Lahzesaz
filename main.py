# RTLTextInput
# ---------------------------------------------------------------------------
class RTLTextInput(TextInput):
    _raw_text = ""
    _updating = False

    def __init__(self, **kwargs):
        super().__init__(**kwargs)
        self.write_tab = False
        self.halign = "right"
        self.base_direction = "rtl"

    def insert_text(self, substring, from_undo=False):
        cursor_index = self.cursor_index()
        self._raw_text = self._raw_text[:cursor_index] + substring + self._raw_text[cursor_index:]
        self._sync_display()

    def do_backspace(self, from_undo=False, mode='bsp'):
        if not self._raw_text:
            return
        cursor_index = self.cursor_index()
        if cursor_index > 0:
            self._raw_text = self._raw_text[:cursor_index - 1] + self._raw_text[cursor_index:]
            self._sync_display()

    def _sync_display(self):
        if self._updating:
            return
        self._updating = True
        raw = self._raw_text
        try:
            if _RESHAPE_AVAILABLE:
                display = arabic_reshaper.reshape(raw)
            else:
                display = raw
            if _BIDI_AVAILABLE:
                display = get_display(display)
        except:
            display = raw
        self.text = display
        self.cursor = (len(display), 0)
        self._updating = False

    def get_raw_text(self):
        return self._raw_text

    def set_raw_text(self, text):
        self._raw_text = text
        self._sync_display()


