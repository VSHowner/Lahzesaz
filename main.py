def make_user_folder(base_path: str, username: str) -> str:
    """
    داخل مسیر انتخاب‌شده یک پوشه به نام کاربر می‌سازد و مسیر کامل را برمی‌گرداند.
    اگر مسیر از نوع content:// (Storage Access Framework اندروید) باشد،
    فعلاً یک راه‌حل best-effort انجام می‌دهیم و رشته را دست‌نخورده نگه می‌داریم؛
    ساختن واقعی زیرپوشه در SAF نیاز به پیاده‌سازی جداگانه با DocumentsContract
    (DocumentFile.createDirectory) از طریق pyjnius دارد و در این نسخه انجام نشده است.
    """
    if not base_path or not username:
        return base_path or ""
    # SAF: نمی‌توان با os.makedirs پوشه ساخت
    if base_path.startswith("content://"):
        # best-effort: مسیر SAF را همان‌طور برمی‌گردانیم و در آینده باید با
        # DocumentsContract پیاده شود.
        return base_path.rstrip("/") + "/" + username
    try:
        full = os.path.join(base_path, username)
        os.makedirs(full, exist_ok=True)
        return full
    except Exception as e:
        print(f"[make_user_folder] خطا: {e}")
        return base_path


