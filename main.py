def fa(text: str) -> str:
    if not text:
        return text
    try:
        if _RESHAPE_AVAILABLE:
            text = arabic_reshaper.reshape(text)
        if _BIDI_AVAILABLE:
            text = get_display(text)
    except Exception as e:
        print(f"[fa] خطا: {e}")
    return text


